Parameter Map（整理確定版）
0. 目的

パラメータの意味・所属・影響範囲を明文化し、誤操作を防ぐ

**「これをONにすれば001相当」**が一目で分かる状態を作る

将来の修正時に「どこを触れば何が変わるか」を固定する

1. パラメータ・グループ構成（固定）
グループA：001再現（入口ルート制御）

目的：入口ルートを Framework001 に確定させる

日本語名	型	推奨（001再現）	影響範囲
001固定モードを有効化	bool	OFF	入口ルート強制（Framework001）
エントリーモード	enum	Mode001	入口ルート候補
001入口抑制構造を有効化	bool	ON	Framework001相当ルート条件
RR緩和構造を有効化	bool	ON	RR Pending有効

補足

001再現では Enable001Mode は OFF が正。

実際に Framework001 に入るかは Spec①の論理式で決まる。

グループB：方向状態（Direction State）

目的：EMAに対する位置状態を安定的に判定

日本語名	型	推奨（001相当）	影響範囲
方向判定デッドゾーン幅（pips）	double	10.0	Neutral帯幅
方向判定ヒステリシス比率	double	0.6	状態戻り幅
方向状態最短維持バー数	int	2	状態遷移抑制
方向判定価格ソース	enum	終値	判定入力

影響

クロス直後／微妙な反転での誤エントリー防止

入口件数・タイミングに直結

グループC：距離制限・再接近（Reapproach Pending）

目的：クロス後の“離れすぎ”を制御

日本語名	型	推奨（001相当）	影響範囲
エントリー最大距離（pips）	double	50.0	即時Entry可否
再接近待機有効バー数	int	36	Pending期限
再接近成立距離（pips）	double	40.0	Pending消費条件
再接近理由タグ	enum	EMAクロス	ログ用

影響

エントリー遅延／見送りの発生条件

Pendingが絡むため 評価順序と密接

グループD：RR緩和（RR Relax Pending）

目的：RR不足時の一時救済

日本語名	型	推奨（001相当）	影響範囲
通常時最小RR比率	double	1.0	基準RR
最小RR緩和を有効化	bool	ON	Pending有効
緩和後最小RR比率	double	0.7	緩和RR
最小RR緩和有効バー数	int	6	Pending期限
RR緩和中再評価方式	enum	同方向・毎バー	再評価頻度

影響

Entryの可否が RR によって変わるケース

入口一致テストで最も差分が出やすい領域

2. 001再現プリセット（公式）

以下を セットで指定した状態を
**「001再現プリセット」**と定義する。

グループA：推奨値すべて

グループB：推奨値すべて

グループC：推奨値すべて

グループD：推奨値すべて

この状態では：

Enable001Mode=True と 入口が完全一致

TEST-2 合格状態

再現検証・将来の回帰テストの 基準点

3. 誤操作防止ルール（運用）

入口検証中は、グループA以外を触らない

グループB〜Dを変えたら
→ それは「最適化」であり、001再現ではない

「入口がズレた」場合は
→ まずグループA（入口ルート）を疑う

